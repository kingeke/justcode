#!/usr/bin/env node
// Generates a self-contained OpenTUI tree-sitter highlight worker for the
// compiled binary.
//
// Why this exists: OpenTUI spawns its parser worker via `new Worker(path)` with a
// path computed at runtime, so `bun build --compile` never pulls the worker (or
// its `web-tree-sitter` dependency and wasm) into the executable. In the shipped
// binary the worker fails to start, `highlightOnce` returns no highlights, and
// all markdown renders raw — literal `**`, no bold, no code highlighting.
//
// The fix: pre-bundle the worker (which inlines `web-tree-sitter` and emits a
// sibling `tree-sitter.wasm`), then inline that wasm into the worker itself by
// seeding Emscripten's `Module.wasmBinary` so it never touches the filesystem.
// The result is one standalone file the app embeds via `import ... with { type:
// 'file' }` and points OpenTUI at through `OTUI_TREE_SITTER_WORKER_PATH`.
//
// Run on demand (and automatically before `build:binary`). Commit the output so
// dev, typecheck, and tests work without a generation step.
import { spawnSync } from 'node:child_process';
import {
  existsSync,
  mkdtempSync,
  readFileSync,
  readdirSync,
  rmSync,
  writeFileSync,
} from 'node:fs';
import { tmpdir } from 'node:os';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..');

// The worker ships at the root of @opentui/core. (Its exports map hides both
// package.json and the entry under node's resolver, so reference it directly.)
const workerEntry = join(
  repoRoot,
  'node_modules/@opentui/core/parser.worker.js'
);
if (!existsSync(workerEntry)) {
  console.error(`Cannot find OpenTUI worker at ${workerEntry}`);
  process.exit(1);
}

const outFile = join(repoRoot, 'apps/cli/src/generated/tree-sitter-worker.js');

const tmp = mkdtempSync(join(tmpdir(), 'jc-ts-worker-'));
try {
  // Bundle the worker so web-tree-sitter is inlined; the wasm comes out as a
  // sibling asset we fold in next.
  const build = spawnSync(
    'bun',
    ['build', workerEntry, '--target=bun', '--outdir', tmp],
    { stdio: 'inherit' }
  );
  if (build.error) {
    console.error(`Failed to run bun: ${build.error.message}`);
    process.exit(1);
  }
  if (build.status !== 0) process.exit(build.status ?? 1);

  const files = readdirSync(tmp);
  const wasmName = files.find((f) => f.endsWith('.wasm'));
  if (!wasmName) {
    console.error('Expected a tree-sitter wasm asset from the worker bundle.');
    process.exit(1);
  }

  const wasmB64 = readFileSync(join(tmp, wasmName)).toString('base64');
  let src = readFileSync(join(tmp, 'parser.worker.js'), 'utf8');

  // Emscripten uses `Module["wasmBinary"]` (raw bytes) before any file lookup, so
  // seeding it makes the worker self-contained — no sibling wasm to resolve at
  // runtime (which a bun --compile binary re-hashes and breaks).
  const needle = 'var wasmBinary = Module["wasmBinary"];';
  if (!src.includes(needle)) {
    console.error(
      'Could not find the Emscripten wasmBinary assignment to patch.\n' +
        'OpenTUI may have changed its worker; update build-tree-sitter-worker.mjs.'
    );
    process.exit(1);
  }
  const inlineBytes =
    'var __OTUI_TS_WASM = Uint8Array.from(atob(' +
    JSON.stringify(wasmB64) +
    '), (c) => c.charCodeAt(0)).buffer;\n';
  src = src.replace(
    needle,
    'var wasmBinary = Module["wasmBinary"] || __OTUI_TS_WASM;'
  );

  const header =
    '// @generated by scripts/build-tree-sitter-worker.mjs — DO NOT EDIT.\n' +
    '// Self-contained OpenTUI tree-sitter worker (web-tree-sitter + wasm inlined)\n' +
    '// so markdown highlighting works inside the bun --compile binary.\n';

  writeFileSync(outFile, header + inlineBytes + src);
  console.log(
    `✓ wrote ${outFile} (${((header.length + inlineBytes.length + src.length) / 1024) | 0} KB)`
  );
} finally {
  rmSync(tmp, { recursive: true, force: true });
}
